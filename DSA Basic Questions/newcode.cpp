#include <iostream>
using namespace std;
int main(){
    //This is direct-line-initialization
    string n{'H','e','l','l','o'};
    //This is copy-initialization
    int k=2;
    //This is direct initialization
    int v(6);
    //Assigning value to a variable where it is defined or decalred is initialization.
    //This is assignment
    int m;
    m=6; 
    //assigning value to the variable after the line where it has been defined or decalred is assignment.
    cout<<n<<endl;
    cout<<k<<"/n";
    cout<<v<<"/n";
    cout<<m<<"/n";
}